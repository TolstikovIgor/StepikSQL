a, b = int(input()), int(input())
count = 0
max_s = 0
for i in range(a, b + 1):
    sum_d = 0
    for j in range(1, i + 1):
        if i % j == 0:
            sum_d = sum_d + j
            if sum_d >= max_s:
                max_s = sum_d
                count = i
